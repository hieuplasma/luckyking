/**
 * Define type of padding that used in project, the values match with key of margin value has been decleared in dimens.tsx
 */
export type PaddingType =
  | 'none'
  | 'tiny'
  | 'small'
  | 'medium'
  | 'large'
  | 'xLarge'
  | 'xxLarge';

export const getMargin = (marginType?: MarginType) => {
  var ret = undefined;
  if (marginType) {
    switch (marginType) {
      case 'tiny':
        ret = Dimension.tinyMargin;
        break;
      case 'small':
        ret = Dimension.smallMargin;
        break;
      case 'medium':
        ret = Dimension.mediumMargin;
        break;
      case 'large':
        ret = Dimension.largeMargin;
        break;
      case 'xLarge':
        ret = Dimension.xLargeMargin;
        break;
      case 'xxLarge':
        ret = Dimension.xxLargeMargin;
        break;
    }
  }
  return ret;
};

